def _make_llm(text="Slide text generated by LLM."):
    provider = MagicMock()
    provider.generate_segment.return_value = text
    return provider


def test_generate_script_empty_plan():
    llm = _make_llm()
    result = generate_script(
        job_id="job-1",
        explain_plan={"sections": []},
        unified_graphs_by_slide={},
        evidence_index={"evidence_items": []},
        entity_to_evidence={},
        llm_provider=llm,
    )
    assert result["schema_version"] == "1.0"
    assert result["job_id"] == "job-1"
    assert result["draft"] is True
    assert result["segments"] == []


def test_generate_script_one_section():
    llm = _make_llm("This slide introduces the architecture.")
    explain_plan = {
        "sections": [
            {"slide_index": 0, "entity_ids": ["ent1"], "evidence_ids": ["ev1"], "section_type": "body"},
        ]
    }
    result = generate_script(
        job_id="job-2",
        explain_plan=explain_plan,
        unified_graphs_by_slide={},
        evidence_index={"evidence_items": [{"evidence_id": "ev1", "slide_index": 0}]},
        entity_to_evidence={"ent1": ["ev1"]},
        llm_provider=llm,
    )
    assert len(result["segments"]) == 1
    seg = result["segments"][0]
    assert seg["text"] == "This slide introduces the architecture."
    assert seg["slide_index"] == 0
    assert "claim_id" in seg


def test_generate_script_segment_claim_ids_are_unique():
    llm = _make_llm()
    sections = [
        {"slide_index": i, "entity_ids": [], "evidence_ids": [], "section_type": "body"}
        for i in range(5)
    ]
    result = generate_script(
        job_id="job-3",
        explain_plan={"sections": sections},
        unified_graphs_by_slide={},
        evidence_index={"evidence_items": []},
        entity_to_evidence={},
        llm_provider=llm,
    )
    claim_ids = [s["claim_id"] for s in result["segments"]]
    assert len(claim_ids) == len(set(claim_ids))


def test_generate_script_evidence_ids_grounded_from_index():
    """When entity_ids and evidence_ids are empty, evidence from slide is pulled in."""
    llm = _make_llm()
    sections = [{"slide_index": 0, "entity_ids": [], "evidence_ids": [], "section_type": "body"}]
    evidence_index = {
        "evidence_items": [
            {"evidence_id": "ev-fallback", "slide_index": 0, "kind": "TEXT_SPAN"},
        ]
    }
    result = generate_script(
        job_id="job-4",
        explain_plan={"sections": sections},
        unified_graphs_by_slide={},
        evidence_index=evidence_index,
        entity_to_evidence={},
        llm_provider=llm,
    )
    seg = result["segments"][0]
    # Grounding: at least one evidence item added from slide
    assert "ev-fallback" in seg["evidence_ids"]


