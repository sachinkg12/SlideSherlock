def test_claim_id_is_deterministic():
    id1 = _claim_id("job-1", 0, 0)
    id2 = _claim_id("job-1", 0, 0)
    assert id1 == id2


def test_claim_id_different_slide_different_id():
    assert _claim_id("job-1", 0, 0) != _claim_id("job-1", 1, 0)


def test_claim_id_different_segment_different_id():
    assert _claim_id("job-1", 0, 0) != _claim_id("job-1", 0, 1)


def test_claim_id_is_sha256():
    cid = _claim_id("job-x", 2, 3)
    expected = hashlib.sha256("job-x|2|3".encode("utf-8")).hexdigest()
    assert cid == expected


