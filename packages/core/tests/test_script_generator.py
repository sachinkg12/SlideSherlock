def test_generate_script_intro_section_context_bundle_used():
    """intro section_type activates context_bundle path when provided."""
    llm = _make_llm()
    bundle = {"_policy": None, "notes": "intro notes"}
    sections = [{"slide_index": 0, "entity_ids": [], "evidence_ids": [], "section_type": "intro"}]

    with patch("script_generator.narration_policy", return_value=("notes", [], False)), \
         patch("script_generator.build_context_bundles_per_slide", return_value={0: bundle}):
        result = generate_script(
            job_id="job-10",
            explain_plan={"sections": sections},
            unified_graphs_by_slide={},
            evidence_index={"evidence_items": [{"evidence_id": "ev-notes", "slide_index": 0}]},
            entity_to_evidence={},
            llm_provider=llm,
            slides_notes_and_text=[("intro notes", "slide text")],
        )

    assert len(result["segments"]) == 1
